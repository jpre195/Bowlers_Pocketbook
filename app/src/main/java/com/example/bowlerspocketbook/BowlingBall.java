package com.example.bowlerspocketbook;

public class BowlingBall {

    private String ballName;
    private String brand;
    private String core;
    private String coverstock;
    private float diff;
    private float rg;
    private float hook;

    public java.lang.String getBallName() {
        return ballName;
    }

    public void setBallName(java.lang.String ballName) {
        this.ballName = ballName;
    }

    public java.lang.String getBrand() {
        return brand;
    }

    public void setBrand(java.lang.String brand) {
        this.brand = brand;
    }

    public java.lang.String getCore() {
        return core;
    }

    public void setCore(java.lang.String core) {
        this.core = core;
    }

    public java.lang.String getCoverstock() {
        return coverstock;
    }

    public void setCoverstock(java.lang.String coverstock) {
        this.coverstock = coverstock;
    }

    public float getDiff() {
        return diff;
    }

    public void setDiff(float diff) {
        this.diff = diff;
    }

    public float getRg() {
        return rg;
    }

    public void setRg(float rg) {
        this.rg = rg;
    }

    public float getHook() {
        return hook;
    }

    public void setHook(float hook) {
        this.hook = hook;
    }
}


